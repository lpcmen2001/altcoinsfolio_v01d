    const checkOrders = function(orders, ordersLog){
        let iterated = 0;
        for (order of orders){
            //console.log(iterated + " out of " + orders.length + " iterations");
            //console.log(order.OrderUuid);
            let currentEpoch = Math.floor(Date.parse(order.Closed)/1000);
            let newValidOrder = true;
            let currentSymbol = null;
            if (order.Exchange == 'USDT-BTC'){
                currentSymbol = 'USDT';
            }else if (order.Exchange == 'BTC-ANS'){
                currentSymbol = 'NEO';
            }
            else {
                currentSymbol = (order.Exchange).substring(4,(order.Exchange).length);
            }
            currentTransactionType = null;
            if (order.OrderType == 'LIMIT_BUY'){
                currentTransactionType = 'buy';
            }else if (order.OrderType == 'LIMIT_SELL'){
                currentTransactionType = 'sell';
            }else{
                console.log('Wrong transaction type');
                console.log('OrderType',order.OrderType);
            }
            let tryToFindInDb = Assets.findOne({symbol: currentSymbol});
            if (tryToFindInDb){
                if(tryToFindInDb.orders){
                    for (orderDb of tryToFindInDb.orders){
                        if (orderDb._id == order.OrderUuid ){
                            //console.log("Order already added in Db, skipping id #" + order.OrderUuid); 
                            newValidOrder = false;
                        }
                    }
                }else{
                    //console.log("Orders of " + tryToFindInDb.symbol + " don't exists.");
                }
            }else{
                if (currentSymbol == 'SEC'){
                    //console.log('Symbol ' + currentSymbol + ' rejected.');
                    newValidOrder = false;
                }
                //cancel invalid orders here
            }
            let finalQuantity = null;
            if (order.QuantityRemaining){
                if (order.QuantityRemaining != 0){
                    finalQuantity = order.Quantity - order.QuantityRemaining;
                }
            }
            else{
                finalQuantity = order.Quantity;
            }
            if (newValidOrder){
                //console.log("Pushed " + order.OrderUuid + " to ordersLog ");
                ordersLog.push({
                    _id: order.OrderUuid,
                    symbol: currentSymbol,
                    qty: finalQuantity,
                    date: order.Closed,
                    epoch: currentEpoch,
                    type: currentTransactionType,
                    feeInSymbol: order.Commission,
                    priceBtc: order.Price
                });
            }
            iterated++;
        }
        updateAssetsWithOrdersHistory(ordersLog);
    }

